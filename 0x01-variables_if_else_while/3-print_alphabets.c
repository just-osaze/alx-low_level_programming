#include <stdio.h>

/**
  * main - Entry point
  * Return: Always success (0)
  */

int main(void)
{
	char lowerCase = 'a';
	char upperCase = 'A';

	while (lowercase <= 'z')
	{
		putchar(lowerCase);
		lowerCase++;
	}

	while (upperCase <= 'Z')
	{
		putchar(uppercase);
		uppercase++;
	}

	putchar("\n");

	return (0);
}
